class PitchToFreq:
    def __init__(self):
        self.note_idx_map = {"C": 0, "C#": 1, "D": 2, "D#": 3, "E": 4, "F": 5, "F#": 6, "G": 7, "G#": 8, "A": 9, "A#": 10, "B": 11}
        self.note_to_freq = [[16, 17, 18, 20, 21, 22, 23, 25, 26, 28, 29, 31],
                             [33, 35, 37, 39, 41, 44, 46, 49, 52, 55, 58, 62],
                             [65, 69, 73, 78, 82, 87, 93, 98, 104, 110, 117, 124],
                             [131, 139, 147, 156, 165, 175, 185, 196, 208, 220, 233, 247],
                             [262, 278, 294, 311, 330, 349, 370, 392, 415, 440, 446, 494],
                             [523, 554, 587, 622, 659, 699, 740, 784, 831, 880, 932, 988],
                             [1047, 1109, 1175, 1245, 1319, 1397, 1475, 1568, 1661, 1760, 1865, 1976],
                             [2093, 2218, 2349, 2489, 2637, 2794, 2960, 3136, 3322, 3520, 3729, 3951],
                             [4186, 4435, 4699, 4978, 5274, 5588, 5920, 6272, 6645, 7040, 7459, 7902]]

    def get_freq(self, n):
        return self.note_to_freq[n.octave][self.note_idx_map[n.note]]


class Note:
    def __init__(self, n, o):
        self.note = n.upper()
        self.octave = o